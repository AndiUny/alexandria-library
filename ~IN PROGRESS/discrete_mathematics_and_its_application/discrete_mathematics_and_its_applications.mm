  <body>
    <p>
      Consider the problem of making n cents change with quarters, dimes, nickels, and pennies, and using the least total number of coins.
    </p>
    <p>
      We can devise a greedy algorithm for making change for n cents by making a locally optimal choice at each step;
    </p>
    <p>
      that is, at each step we choose the coin of the largest denomination possible to add to the pile of change without exceeding n cents.
    </p>
    <p>
      <br />
      For example, to make change for 67 cents, we first select a quarter (leaving 42 cents).
    </p>
    <p>
      We next select a second quarter (leaving 17 cents), followed by a dime (leaving 7 cents), followed by a nickel (leaving 2 cents), followed by a penny (leaving 1 cent), followed by a penny.
    </p>
